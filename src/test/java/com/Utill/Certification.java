package com.Utill;

public class Certification {
	private String certification_points;
	private String certification_level;
	private String date_certified;
	private String rating_system;
	

	public String getRating_system() {
		return rating_system;
	}
	public void setRating_system(String rating_system) {
		this.rating_system = rating_system;
	}
	public String getCertification_points() {
		return certification_points;
	}
	public void setCertification_points(String certification_points) {
		this.certification_points = certification_points;
	}
	public String getCertification_level() {
		return certification_level;
	}
	public void setCertification_level(String certification_level) {
		this.certification_level = certification_level;
	}
	public String getDate_certified() {
		return date_certified;
	}
	public void setDate_certified(String date_certified) {
		this.date_certified = date_certified;
	}


	
	
	

}
